"""Public output models."""
from __future__ import annotations
from dataclasses import dataclass, field
from typing import Literal


@dataclass
class Source:
    """
    Developer-provided ground truth document.

    Attributes:
        content: The full text of the source document.
        source_id: A unique identifier, e.g. "contract_v3.pdf".
        source_type: Hint for source-type-aware routing. Defaults to "document".
        page_hint: Optional location hint, e.g. "page 4" or "section 3.2".
        section_id: Optional section identifier within the document.
        derived_from_llm: Whether this source was generated by an LLM.
        original_document_id: ID of the originating document if this is a derived chunk.
        as_of_date: ISO date string indicating when the source was current.
        prev_context: Text immediately preceding this chunk (for context).
        next_context: Text immediately following this chunk (for context).
    """

    content: str
    source_id: str
    source_type: str = "document"
    page_hint: str | None = None
    section_id: str | None = None
    derived_from_llm: bool = False
    original_document_id: str | None = None
    as_of_date: str | None = None
    prev_context: str | None = None
    next_context: str | None = None


@dataclass
class AtomicClaimResult:
    """
    Result for a single atomic claim extracted from the full claim.

    Attributes:
        claim_text: The atomic claim sentence.
        claim_type: Whether it is Extractive or Inferential.
        status: The verdict for this specific atom.
        source_id: Which source document supported or contradicted this atom.
        source_excerpt: Verbatim text excerpt from source (Extractive claims only).
        reasoning_basis: LLM reasoning chain (Inferential claims only).
        page_hint: Location hint from the source, if available.
    """

    claim_text: str
    claim_type: Literal["Extractive", "Inferential"]
    status: Literal["VERIFIED", "CONTRADICTED", "UNVERIFIABLE"]
    source_id: str | None = None
    source_excerpt: str | None = None
    reasoning_basis: list[str] | None = None  # FIX-04: PRD specifies list[str], not str
    page_hint: str | None = None


@dataclass
class VerificationResult:
    """
    The public output of verify(), averify(), verify_batch(), and verify_structured().

    Attributes:
        is_valid: True if the claim is verified; False otherwise.
        overall_verdict: Human-readable summary verdict.
        verification_method: How the result was determined.
        atomic_claims: Per-atom results.
        factual_consistency_score: 0.0–1.0 score.
        sources_used: Deduplicated list of source_ids that contributed to verdict.
        rationale: LLM rationale or brief explanation.
        offending_claim: The first claim atom that caused a non-VERIFIED result, if any.
        status: Machine-readable status code.
        total_cost_usd: Total LLM cost for this verification call.
    """

    is_valid: bool
    overall_verdict: str
    verification_method: Literal["tier2_lexical", "tier3_llm", "skipped"]
    atomic_claims: list[AtomicClaimResult]
    factual_consistency_score: float
    sources_used: list[str]
    rationale: str
    offending_claim: str | None
    status: Literal[
        "VERIFIED",
        "CONTRADICTED",
        "UNVERIFIABLE",
        "SKIPPED_DUE_TO_COST",
        "PARSE_ERROR",
        "ERROR",
    ]
    total_cost_usd: float


@dataclass
class Citation:
    """A citation linking a claim to a specific excerpt in a source document."""

    source_id: str
    excerpt: str | None = None
    excerpt_char_start: int | None = None
    excerpt_char_end: int | None = None
    citation_confidence: float | None = None
    page_hint: str | None = None
    section_id: str | None = None


@dataclass
class ContextualizedClaimUnit:
    """A single claim unit with enrichment metadata and verification state."""

    display_text: str
    claim_text: str
    enrichment_method: str | None = None
    structural_type: str | None = None
    heading_path: list = field(default_factory=list)
    verification_status: str = "PENDING"
    citation: Citation | None = None
    confidence: float = 0.0
    numerical_fast_exit: bool = False


@dataclass
class GroundingResult:
    """
    Aggregate result of a grounding evaluation over one or more claim units.

    First 4 fields are required; remaining fields have defaults.
    """

    is_grounded: bool
    score: float
    status: str
    evaluation_method: str
    total_units: int = 0
    grounded_units: int = 0
    ungrounded_units: int = 0
    unverifiable_units: int = 0
    unit_results: list = field(default_factory=list)
    sources_used: list = field(default_factory=list)
    total_cost_usd: float = 0.0
    summary: str = ""
    provenance_warning: bool = False
    units_capped: bool = False
    numerical_fast_exits: int = 0
    majority_votes_triggered: int = 0
    audit_records: list | None = None


@dataclass
class VerificationAuditRecord:
    """Immutable audit record capturing full traceability for a single claim verification."""

    boundary_id: str
    claim_text: str
    verdict: str
    tier_path: list
    model: str
    cost_usd: float
    timestamp_utc: str
    profile_name: str
    evidence_bundle: list | None = None
    winning_citation: Citation | None = None
    majority_vote_triggered: bool = False
    vote_breakdown: dict | None = None
    defined_terms_resolved: list = field(default_factory=list)
    cross_references_injected: list = field(default_factory=list)
    profile_override: bool = False
    effective_faithfulness_threshold: float | None = None
